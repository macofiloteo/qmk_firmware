uint32_t uptime_timer = 0;

bool oled_task_kb(void) {
    if (!oled_task_user()) {
        return false;
    }
    if (uptime_timer == 0) {
        uptime_timer = timer_read32();
    }
    char     uptime_str[12];
    uint32_t uptime  = timer_elapsed32(uptime_timer) / 1000;
    uint32_t minutes = uptime / 60;
    uint32_t hours   = minutes / 60;
    sprintf(uptime_str, " %lu:%02lu:%02lu", hours, minutes % 60, uptime % 60);
    led_t led_state = host_keyboard_led_state();
    // render_bongocat();
    // oled_set_cursor(14, 0); // sets cursor to (column, row) using charactar spacing (4 rows on 128x32 screen, anything more will overflow back to the top)
    // oled_write_P(PSTR("WPC:"), false);
    // oled_write(get_u8_str(get_current_wpm(), '0'), false); // writes wpm on top right corner of string
    if (led_state.caps_lock) {
        oled_set_cursor(13, 0);
        oled_write_P(PSTR(" CAPS ON"), false);
    } else {
        oled_set_cursor(13, 0);
        oled_write_P(PSTR("CAPS OFF"), false);
    }
    // Yes, you can improve this by just getting the number of digits then subtract it to 14
    // But I like to keep it simple since I wont be
    // seeing this code for the next 10 years lol
    if (hours > 99) {
        oled_set_cursor(11, 2);
    } else if (hours > 9) {
        oled_set_cursor(12, 2);
    } else {
        oled_set_cursor(13, 2);
    }
    oled_write_P(PSTR(uptime_str), false);
    return false;
}

